"""ðŸŽ¯ Goal:
Ask the user to enter names one by one. Separate names into two lists based on a simple rule:

If the name starts with a vowel â†’ go to vowel_names

Else â†’ go to consonant_names

Keep looping until they type 'done'.

ðŸ§  Requirements Recap:
Use a while loop

Use .lower() to make checks case-insensitive

Use .startswith() for checking vowels

End loop when user types "done"

"""

vowel_names = []
consonant_names = []

while True:
    names = input("Enter a name: ")
    names = names.lower()

    if names == "done":
        break

    vowels = ["a", "e", "i", "o", "u"]
    first_letter = names[0]

    if first_letter in vowels:
        vowel_names.append(names)
    else:
        consonant_names.append(names)

print("Vowel Names:", vowel_names)
print("Consonant Names:", consonant_names)

